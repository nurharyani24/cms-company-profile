    <p>The Audit Committee is responsible for providing opinions to the Board of Commissioners on reports or other items that are submitted by the Board of Directors to the Board of Commissioners, for identifying items that need the attention of the Board of Commissioners, and executing other tasks that are related to the tasks of the Board of Commissioners, among others including:</p>
    <ol type="a">
    <li>Reviewing the financial information that will be issued  as the Company’s financial statements, projections, and other financial information;</li>
    <li>To review the Company’s compliance with Capital  Market regulations and other laws and regulations related to the Company’s activities;</li>
    <li>To review the implementation of audit by the internal auditor towards the Company;</li>
    <li>To report to the Board of Commissioners on various risks faced by  the Company and the implementation of risk management by the Board of Directors;</li>
    <li>To review and report to the Board of Commissioners on complaints relating to the Company; and</li>
    <li>To maintain the confidentiality of documents, data and information owned by the Company.</li>
    </ol>
    
    <h3>THE AUDIT COMMITTEE MEMBERSHIP</h3>
    <p>Based on the Decree of the Board of Commissioners No.SK.005 DEKOM/XII/2014 dated December 22, 2014, the Company has an Audit Committee which is chaired by an Independent Commissioner and 2 (two) members from outside of the Company, namely:</p>
    <ol>
    <li>Ilham Akbar Habibie as Chairman</li>
    <li>Ridwan Amsori as a member; and</li>
    <li>Arydhian B. Djamin as member</li>
    </ol>
    
    <h3>TERM OF SERVICE</h3>
    <p>The term of service of the Head of the Audit Committee is the same as his term of service as an Independent Commissioner, whereas members of the Committee are appointed for a term of service of 3 (three) years.</p>
    
    <h3>THE MEMBERS OF AUDIT COMMITTEE PROFILE</h3><br />
    <h3>ILHAM A. HABIBIE</h3>
    <p>He has served as Chairman of the Company’s Audit Committee since February 2014. The profile of Mr. Ilham A. Habibie may be seen in the Profile of the Board of Commissioners section.</p>
    
    <h3>RIDWAN AMSORI</h3>
    <p>Indonesian citizen, born in Jakarta in 1980, earned a degree in Economics from Trisakti University majoring in Accounting. He served as a member of the Audit Committee of the Company since February 2014 up to this moment. Previously he served as program coordinator for the Yayasan Satu Untuk Negeri tvOne (2012–February 2014), Junior Manager Auditor PT Bakrie Global Ventura (2012- February 2014), Senior Auditor PT Capital Manager Asia Indonesia (2011–2012), Manager of Consumer Finance PT CIMB Niaga Tbk. (2006–2010), Sales PT Bank Niaga Tbk. (2004–2006), Accounting Officer PT Rumsitor Tehnik (2003–2004).</p>
    
    <h3>ARYDHIAN B. DJAMIN</h3>
