        <h1 class="judul">Piagam Komite Audit</h1>
	</div>
    
    <h3>PIAGAM KOMITE AUDIT</h3>
    <h3>PT INTERMEDIA CAPITAL, Tbk.</h3>
    <ul>
    <ol type="1">
    
    
    <li>Definisi
        <ul>
        <ol type="a">
        <li>Komite Audit adalah komite yang dibentuk oleh Dewan Komisaris dalam rangka membantu pelaksanaan tugas dan fungsinya.</li>
    
        <li>Komisaris Independen adalah anggota Komisaris yang:
            <ul>
            <ol type="1">
                <li>Berasal dari luar Perusahaan;</li>
    
                <li>Tidak mempunyai saham baik langsung maupun tidak langsung pada Perusahaan;</li>
    
                <li>Tidak mempunyai hubungan Afiliasi dengan Perusahaan, Komisaris, Direksi, atau Pemegang Saham Utama Perusahaan; dan</li>
    
                <li>Tidak memiliki hubungan usaha baik langsung mapun tidak langsung yang berkaitan dengan kegiatan usaha Perusahaan.</li>
            </ol>
            </ul>
        </li>
        <li>Perseroan/Perusahaan berarti PT Intermedia Capital,Tbk.</li>
    
        </ol>
        </ul>
        
    </li>
    
    <li>Dasar Hukum Pembentukan Komite Audit
        <ul>
        <ol type="a">
            <li>Undang-undang No. 40 tahun 2007 tentang Perseroan Terbatas;</li>
    
            <li>Peraturan Bapepam dan LK Nomor IX.I.5 tentang Pembentukan dan Pedoman Pelaksanaan Kerja Komite Audit;</li>
    
            <li>Keputusan Direksi PT Bursa Efek Jakarta No. Kep-305/BEJ/07-2004 tertanggal   19 Juli 2004 tentang Peraturan Nomor I-A tentang Pencatatan Saham dan Efek bersifat Ekuitas selain Saham yang diterbitkan oleh Perusahaan Tercatat;</li>
    
            <li>Anggaran Dasar Perusahaan;</li>
    
            <li>Peraturan Perusahaan.</li>
        </ol>
        </ul>
    
    </li>
    
    <li>Maksud dan Tujuan
        
    
    <p>Komite Audit dibentuk dengan tujuan untuk membantu Dewan Komisaris dalam menjalankan peran pengawasan di Perusahaan, terutama dalam hal :</p>
        <ul>
        <ol>
        <li>meningkatkan kualitas Laporan Keuangan Perusahaan;</li>
    
        <li>memastikan efektivitas sistem pengendalian internal yang dapat mengurangi kesempatan terjadinya penyimpangan dalam pengelolaan Perusahaan;</li>
    
        <li>mengidentifikasi hal-hal yang memerlukan perhatian Dewan Komisaris, termasuk keputusan Perusahaan terhadap undang-undang dan peraturan yang berlaku;</li>
    
        <li>mengawasi kemandirian fungsi internal dan eksternal audit yang ditunjuk Perusahaan.</li>
        </ol>
        </ul>
     
    </li>
    
    <li>Struktur dan Keanggotaan Komite Audit
        <ul>
        <ol type="1">
            <li>Struktur Komite Audit
                <ul>
                <ol type="a">
                    <li>Anggota Komite Audit diangkat dan diberhentikan oleh Dewan Komisaris dan dilaporkan kepada Rapat Umum Pemegang Saham;</li>
    
                    <li>Anggota Komite Audit yang merupakan Komisaris Independen bertindak sebagai Ketua Komite Audit.  Dalam hal Komisaris Independen yang menjadi anggota Komite Audit lebih dari satu orang maka salah satunya bertindak sebagai Ketua Komite Audit.</li>
                <ol>
                </ul>
                
            </li>
            <li>Persyaratan Keanggotaan Komite Audit
                <ul>
                <ol type="a">
                <li>Memiliki integritas yang tinggi, kemampuan, pengetahuan dan pengalaman yang memadai sesuai dengan latar belakang pendidikannya, serta mampu berkomunikasi dengan baik;</li>
    
                <li>Salah seorang dari anggota Komite Audit harus memiliki latar belakang pendidikan akuntansi atau keuangan;</li>
    
                <li>Memiliki pengetahuan yang cukup untuk membaca dan memahami laporan keuangan;</li>
    
                <li>Memiliki pengetahuan yang memadai tentang peraturan perundangan di bidang pasar modal dan peraturan perundang-undangan terkait lainnya;</li>
    
                <li>Bukan merupakan orang dalam Kantor Akuntan Publik, Kantor Konsultan Hukum, atau Pihak lain yang memberi jasa audit, jasa non audit dan atau jasa konsultasi lain kepada Perusahaan dalam waktu 6 (enam) bulan terakhir sebelum diangkat oleh Dewan Komisaris;</li>
    
                <li>Bukan merupakan orang yang mempunyai wewenang dan tanggung jawab untuk merencanakan, memimpin, atau mengendalikan kegiatan Perusahaan dalam waktu 6 (enam) bulan terakhir sebelum diangkat oleh Dewan Komisaris, kecuali Komisaris Independen;</li>
    
                <li>Tidak mempunyai saham baik langsung maupun tidak langsung pada Perusahaan. Dalam hal anggota Komite Audit memperoleh saham akibat suatu peristiwa hukum maka dalam jangka waktu paling lama 6 (enam) bulan setelah diperolehnya saham tersebut wajib mengalihkan kepada Pihak lain;</li>
                
                
                <li>Tidak mempunyai:
                    <ul>
                    <ol type="1">
                    <li>Hubungan keluarga karena perkawinan dan keturunan sampai derajat kedua, baik secara horizontal maupun vertical dengan Komisaris, Direksi, atau Pemegang Saham Utama Perusahaan; dan atau</li>
    
                    <li>Hubungan usaha baik langsung maupun tidak langsung yang berkaitan dengan kegiatan usaha Perusahaan.</li>
                    <ol>
                    </ul>
                </li>
                <ol>
                </ul>
            </li>	
                
            <ol>
            </ul>
    
    </li>
    
    <li>Tugas, wewenang dan Tanggung Jawab Komite Audit
        <ul>
        <ol type="1">
        <li>Tugas dan Tanggung jawab Komite Audit
        
        <p>Komite Audit bertugas untuk memberikan pendapat kepada Dewan Komisaris terhadap laporan atau hal-hal yang disampaikan oleh Direksi kepada Dewan Komisaris, mengidentifikasi hal-hal yang memerlukan perhatian Komisaris, dan melaksanakan tugas-tugas lain yang berkaitan dengan tugas Dewan Komisaris, antara lain meliputi:</p>
            <ul>
            <ol type="a">
            <li>Melakukan penelaahan atas informasi keuangan yang akan dikeluarkan Perusahaan seperti laporan keuangan, proyeksi, dan informasi keuangan lainya;</li>
    
            <li>Melakukan penelaahan atas ketaatan Perusahaan terhadap peraturan perundang-undangan di bidang Pasar Modal dan peraturan perundang-undangan lainnya yang berhubungan dengan kegiatan perusahaan;</li>
    
            <li>Melakukan penelaahan atas pelaksanaan pemeriksaan oleh auditor internal terhadap Perusahaan;</li>
    
            <li>Melaporkan kepada Dewan Komisaris berbagai risiko yang dihadapi Perusahaan dan pelaksanaan manajemen risiko oleh Direksi;</li>
    
            <li>Melakukan penelaahan dan melaporkan kepada Dewan Komisaris atas pengaduan yang berkaitan dengan Perusahaan; dan</li>
    
            <li>Menjaga kerahasiaan dokumen, data dan informasi yang dimiliki Perusahaan.</li>
            </ol>
            </ul>
            
        </li>
    
        <li>Wewenang Komite Audit
            <ul>
            <ol type="a">
            <li>Komite Audit berwenang untuk mengakses catatan dan informasi tentang karyawan, dana, asset serta sumber daya Perusahaan lainnya yang berkaitan dengan pelaksanaan tugasnya; dan</li>
    
            <li>Dalam melaksanakan wewenang, Komite Audit wajib bekerja sama dengan pihak yang melaksanakan fungsi internal audit.</li>
            </ol>
            </ul>
        </li>
        </ol>
        </ul>
    </li>
    
    <li>Rapat Komite Audit
        <ul>
            <ol type="1">
            <li>Komite Audit mengadakan rapat sekurang-kurangnya sama dengan ketentuan minimal rapat Dewan Komisaris yang ditetapkan dalam Anggaran Dasar Perusahaan; dan</li>
    
            <li>Setiap rapat Komite Audit dituangkan dalam risalah rapat yang ditandatangani oleh seluruh anggota Komite Audit yang hadir dalam rapat tersebut.</li>
        </ol>
            </ul>
    </li>
    
    <li>Pelaporan
        <ul>
            <ol type="1">
            <li>Komite Audit membuat laporan kepada Dewan Komisaris atas setiap penugasan yang diberikan; dan</li>
    
            <li>Komite Audit membuat laporan tahunan pelaksanaan kegiatan Komite Audit kepada Dewan Komisaris.</li>
        </ol>
            </ul>
    </li>
    <li>Masa Tugas
    
    <p>Masa tugas anggota Komite Audit tidak boleh lebih lama dari masa jabatan Dewan Komisaris sebagaimana diatur dalam Anggaran Dasar Perusahaan dan dapat dipilih kembali hanya untuk 1 (satu) periode berikutnya. </p>
    </li>
    
    <li>Lain - Lain	
        <ul>
        
        <li>Hal-hal yang belum cukup diatur dalam Piagam Komite Audit ini akan ditetapkan kemudian secara tertulis oleh Dewan Komisaris Perusahaan; dan</li>
    
        <li>Piagam Komite Audit ini berlaku terhitung sejak tanggal ditetapkan.</li>
        </ul>
    </li>
    
    </ol>
    </ul>
        
