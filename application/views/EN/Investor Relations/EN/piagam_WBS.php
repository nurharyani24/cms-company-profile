    I.	SISTEM PELAPORAN PELANGGARAN
     
    A.	RUANG LINGKUP
     
    Sistem pelaporan pelanggaran merupakan sistem  yang mengelola Pengaduan/ Penyingkapan mengenai perilaku melawan hukum, perbuatan tidak etis/tidak semestinya  secara rahasia, anonim dan mandiri (Independent) yang digunakan untuk mengoptimalkan  peran secara Insan MDIA dan pihak lainnya dalam mengungkapkan pelangggaran yang terjadi di lingkungan Perseroan. Sistem Pelaporan Pelanggaran digunakan apabila Pengaduan/Pengungkapan dianggap tidak efektif untuk disalurkan melalui jalur formal (melalui atasan langsung atau ditindak-lanjuti oleh sistem  fungsi terkait).
    Lingkup Pengaduan/Penyingkapan yang akan dianggap sebagai pelaporan pelanggaran adalah tindakan yang dapat merugikan Perseroan baik secara finansial maupun citra, reputasi dan nama baik Perseroan, antara lain:
    1.	Penyimpangan dari Pedoman Etika Perusahaan (Code of Conduct) dan peraturan  perundang-undangan yang berlaku;
    2.	Penyalahgunaan jabatan untuk kepentingan lain di luar Perseroan;
    3.	Pemerasan dan/atu Perbuatan Curang;
    4.	Transaksi Benturan Kepentingan; dan
    5.	Penyuapan/Gratifikasi.
    
    B.	PRINSIP DASAR
    
    Setiap Pengaduan/Penyingkapan yang dilakukan oleh Pelapor harus memperhatikan hal-hal sebagai berikut:
    1. 	Pelapor wajib memberikan indikasi awal yang dapat dipertanggungjawabkan, meliputi:
    a.	pelanggaran yang diadukan, meliputi  jumlah kerugian (apabila dapat ditentukan 1 (satu) Pengaduan/Penyingkapan sebaiknya hanya untuk 1 (satu) pelanggaran agar penanganannya dapat lebih focus;
    b.	pihak yang terlibat, yaitu siapa yang seharusnya bertanggungjawab atas pelanggaran tersebut, termasuk saksi- saksi dan pihak yang diuntungkan atau dirugikan atas pelanggaran tersebut;
    c.	Lokasi Pelanggaran, yaitu meliputi nama, tempat, atau fingsi terjadinya pelanggaran tersebut;
    d.	waktu pelanggaran, yaitu periode pelanggaran baik berupa hari, mingggu, bulan, tahun atau tangggal tertentu pada saat pelanggaran tersebut terjadi;
    e.	bagaimana terjadinya pelanggaran tersebut dan apakah terdapat bukti-bukti pendukung tentang terjadinya  pelanggaran;
    f.	apakah pelanggaran tersebut pernah terjadi sebelumnya.
    2. 	untuk mempercepat dan mempermudah proses tindak lanjut Pengaduan/ Penyingkapan, maka pelapor dianjurkan untuk memberikan informasi mengenai data diri, sekurang-kurangnya memuat alamat/nomor telepon/handphone/faksimili/email.
    
    C.	PERLINDUNGAN TERHADAP PELAPOR
    1.	Identitas pelapor dijamin kerahasiaan oleh Perseroan.
    
    2.	Perseroan menjamin perlindungan terhadap Pelapor dari segala bentuk ancaman, intimidasi, ataupun tindakan tidak menyenangkan dari pihak manapun selama pelapor enjaga kerahasiaan pelanggaran yang diadukannya kepada pihak manapun.
    
    3.	perlindungan terhadap Pelapor juga berlaku bagi para pihak yang melaksanakan investigasi maupun pihak-pihak yang memberikan informasi tambahan terkait dengan Pengaduan/ Penyingkapan tersebut.
    
    
    C.	KEWENANGAN PENANGANAN LAPORAN
    1.	Pengaduan/Penyingkapan pelanggaran yang berkaitan dan atau dilakukan oleh komisi Pelaporan pelanggaran dan Tim Investigasi akan ditindaklanjuti oleh Direksi.
    2.	Pengaduan/Penyingkapan pelanggaran yang berkaitan dan atau dilakukan oleh Direksi maka Pelaporan pelanggaran dan Tim Investigasi akan ditindaklanjuti oleh Dewan Komisaris.
    3.	Pengaduan/Penyingkapan pelanggaran yang berkaitan dan atau dilakukan oleh Dewan Komisaris akan ditindaklanjuti oleh Direksi.
    
    E. LAPORAN KOMISI LAPORAN PELANGGARAN
    Komisi Pelaporan Pelanggaran wajib membuat laporan secara berkala kepada Direksi yaitu setiap 3 (tiga) bulan sekali, yang disampaikan selambat-lambatnya minggu kedua pada bulan berikutnya yang meliputi jumlah Pengaduan/ Penyingkapan, kategori Pengaduan/Penyingkapan serta media yang digunakan oleh Pelapor dan penyampaiannnya.
    
    II.	PENGELOLAAN SISTEM PELAPORAN PELANGGARAN
    A.	SARANA/ MEDIA PELAPORAN
    Pelapor menyampaikan Pengaduan/Penyingkapan pelanggaran kepada Komisi Pelaporan Pelanggaran melalui sarana/media telepon,website dan faksimili Perseroan, sampai dengan adanya yang khusus diperuntukkan bagi Sistem Pengelolaan Pelanggaran tersebut.
    
    B.	PROSEDUR PENGELOLAAN
    1.	pengelola administrasi Sistem Pelaporan Pelanggaran menerima dan menyaring setiap laporan Pengaduan/Penyingkapan pelanggaran yang diterima, apakah terdapat indikasi awal atau sesuai dengan kriteria laporan Sistem  Pelaporan Pelanggaran dan dapat ditindak lanjuti? Bila YA laporan Pengaduan/Penyingkapan diteruskan ke Komisi Pelaporan Pelanggaran, bila TIDAK proses sistem Pelaporan pelanggaran selesai.
    
    2.	Komisi Pelaporan Pelanggaran menerima laporan dari pengelola administrasi Sistem Pelaporan pelanggaran dan melakukan investigasi awal terhadap setiap Pengaduan/Penyingkapan. Hasil investigasi awal tersebut dilaporkan kepada direksi.
    
    3.	dari laporan Komisi Pelaporan Pelanggaran, Direksi menetapkan rekomendasi apakah akan dilakukan investigasi lanjutan oleh Tim Investigasi  (satuan pengawasan intern dan/ atau Eksternal investigator), serta melaporkan hasil keputusan tersebut  kepada Dewan Komisaris.
    
    4.	Tim investigasi (satuan pengawasan intern dan/atau Eksternal Investigator) melakukan Investigasi lanjut terhadap Pengaduan/Penyingkapan dan melaporkan hasilnya kepada Direksi.
    
    5.	Dari laporan investigasi lanjutan oleh tim investigasi, Direksi menetapkan rekomendasi  tindakan selanjutnya. Bila tidak terbukti atau selesai, maka laporan Pengaduan/ Penyingkapan akan ditutup. Apabila Pengaduan/Penyingkapan tersebut terbukti atau memerlukan tindaklanjut, maka akan dikenakan sanksi sesuai ketentuan yang berlaku, atau diteruskan kepada pihak penyidik untuk proses lebih lanjut  sesuai ketentuan dan peraturan yang berlaku.
    
    6.	seluruh proses investigasi atas Pengaduan/Penyingkapan wajib dituangkan dalam suatu Berita Acara dan seluruh proses Sistem Pelaporan Pelanggaran harus terdokumentasi dengan baik dan reliable (dapat dipertanggungjawabkan).
    
    
    D.	SANKSI
    Bentuk sanksi terhadap terlapor yang telah terbukti melakukan pelanggaran akan ditentukan sesuai dengan ketentuan dan peraturan yang berlaku di Perseroan.
     
    
    
