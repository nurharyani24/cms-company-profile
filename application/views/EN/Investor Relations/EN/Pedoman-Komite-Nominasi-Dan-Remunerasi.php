    	<li>PENGANTAR </li>
    
    	<p>Dewan Komisaris membentuk Komite Nominasi dan Remunerasi yang bertugas membantu Dewan Komisaris melaksanakan tugas dan fungsi pengawasan yang terkait dengan sistem nominasi dan remunerasi bagi anggota Direksi dan Dewan Komisaris.</p> 
    
    	<p>Pedoman Komite Nominasi dan Remunerasi ini (“Pedoman”) disusun untuk memberikan arahan kepada Komite Nominasi dan Remunerasi dan anggotanya dalam menjalankan tugas dan tanggung jawabnya. </p>
    
    
    	<li>TUGAS DAN TANGGUNG JAWAB KOMITE NOMINASI DAN REMUNERASI</li>
    
    	<p>Komite Nominasi dan Remunerasi mempunyai tugas dan tanggung jawab:</p>
        <ul>
    	<ol type="a">
        
    	<li>Terkait dengan Fungsi Nominasi 
        	<ul>
            <ol type="1">
    			<li>Memberikan rekomendasi kepada dan/atau membantu Dewan Komisaris mengenai:
                	<ul>
            		<ol type="i">
                    <li>Komposisi jabatan anggota Direksi dan/atau Dewan Komisaris; </li> 
                    <li>Kebijakan dan kriteria yang dibutuhkan dalam proses nominasi anggota Direksi dan/atau anggota Dewan Komisaris; </li> 
                    <li>Kebijakan evaluasi kinerja bagi anggota Direksi dan/atau anggota Dewan Komisaris; dan</li> 
                    <li>Sistem serta prosedur pemilihan dan/atau penggantian anggota Dewan Komisaris dan Direksi kepada Dewan Komisaris untuk disampaikan kepada Rapat Umum Pemegang Saham (“RUPS”). </li> 
                	</ol>
                    </ul>
                </li> 
            
    	</li>
    
    	<li>Membantu Dewan Komisaris melakukan penilaian kinerja anggota Direksi dan/atau anggota Dewan Komisaris berdasarkan tolok ukur yang telah disusun sebagai bahan evaluasi ;</li>
    
    	<li>Memberikan rekomendasi kepada Dewan Komisaris mengenai program pengembangan kemampuan anggota Direksi dan/atau anggota Dewan Komisaris; dan</li>
    
    	<li>Memberikan usulan calon yang memenuhi syarat sebagai anggota Direksi dan/atau anggota Dewan Komisaris kepada Dewan Komisaris untuk disampaikan kepada RUPS  untuk mendapatkan persetujuan.</li>
    	</ol>
        </ul>
    
    <li>Terkait dengan Fungsi Remunerasi 
    	<ul>
        <ol>
        <li>Memberikan rekomendasi kepada dan/atau membantu Dewan Komisaris mengenai: 
        	<ul>
            <ol type="i">
                <li>Struktur remunerasi anggota Direksi dan anggota Dewan Komisaris; </li>
                <li>Kebijakan atas remunerasi anggota Direksi dan anggota Dewan Komisaris; dan </li>
                <li>Besaran atas remunerasi anggota Direksi dan anggota Dewan Komisaris.</li>
        	</ol>
            </ul> 
        </li>
        <li>Membantu Dewan Komisaris dalam melakukan penilaian kesesuaian remunerasi yang diterima masing-masing anggota Direksi dan/atau anggota Dewan Komisaris terkait dengan kinerja mereka.</li> 
        </ol>
        </ul>
    </li>
    <li>Tugas dan tanggung jawab Komite Nominasi dan Remunerasi dilaksanakan sebagaimana diatur dalam Bagian 5 di bawah. </li>
    
    <li>Dewan Komisaris dapat memberikan kuasa kepada Komite Nominasi dan Remunerasi untuk melaksanakan tugas dan tanggung jawab tertentu dari Dewan Komisaris sehubungan dengan fungsi nominasi dan remunerasinya. </li>
    
    <li>Komite Nominasi dan Remunerasi wajib bertindak independen dalam melaksanakan tugas dan tanggung jawabnya sebagaimana diatur dalam Pedoman ini. </li>
    
    <li>Setiap anggota Komite Nominasi dan Remunerasi dilarang mengambil keuntungan pribadi, baik secara langsung maupun tidak langsung, dari kegiatan Perseroan. </li>
    
    
