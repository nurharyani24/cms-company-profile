    <li>KOMPOSISI DAN STRUKTUR KEANGGOTAAN 
    	<ul>
        <ol type="a">
        
    	<li>Komite Nominasi dan Remunerasi paling kurang terdiri dari 3 (tiga) orang, dengan ketentuan sebagai berikut: 
    		<ul>
            <ol>
        	<li>1 (satu) orang anggota yang bertindak sebagai ketua dan yang merupakan Komisaris Independen. </li>
        
        <li>Pihak yang dapat diangkat sebagai anggota lainnya berasal dari: 
        	</ol>
            </ul>
        
        (i)	anggota Dewan Komisaris Perseroan yang sedang menjabat; dan/atau 
        (ii)	pihak yang berasal dari luar Perseroan; dan/atau 
        (iii)	pihak yang menduduki jabatan manajerial di bawah Direksi yang membidangi sumber daya manusia.
        </li>
    	</li>
    b.	Struktur Keanggotaan
    
    1)	Ketentuan bahwa sebagian besar anggota Komite Nominasi dan Remunerasi sebagaimana dimaksud dalam Bagian 3.a ini tidak dapat berasal dari pihak yang menduduki jabatan manajerial di bawah Direksi yang membidangi sumber daya manusia. 
    
    2)	Jika terdapat pihak yang berasal dari luar Perseroan sebagaimana dimaksud dalam Bagian 3.a di atas yang akan diangkat sebagai anggota Komite Nominasi dan Remunerasi, wajib memenuhi syarat : 
    
    (i)	Tidak mempunyai hubungan afiliasi dengan Perseroan, anggota Direksi, anggota Dewan Komisaris, atau pemegang saham utama Perseroan;
    (ii)	Memiliki pengalaman terkait nominasi dan/atau remunerasi; dan
    (iii)	Tidak merangkap jabatan sebagai anggota komite lainnya yang dimiliki Perseroan
    
    3)	Anggota Direksi Perseroan tidak dapat menjadi anggota Komite Nominasi dan Remunerasi. 
    
    c.	Anggota Komite Nominasi dan Remunerasi wajib memiliki integritas yang tinggi dan kemampuan, pengetahuan dan pengalaman yang memadai. Anggota Komite Nominasi dan Remunerasi harus mampu bekerja sama dan berkomunikasi dengan baik dan dapat menyediakan waktu yang cukup untuk melaksanakan tugas dan tanggung jawab Komite Nominasi dan Remunerasi. 
    </ol>
    </ul>
    </li>
    4.   	TATA CARA DAN PROSEDUR KERJA 
    
    a.	Terkait dengan Fungsi Nominasi 
    
    Dalam melaksanakan fungsi tugas dan tanggung jawabnya sebagaimana disebutkan dalam Bagian 2 di atas, Komite Nominasi dan Remunerasi  wajib melakukan hal-hal berikut: 
    
    1)	Mengidentifikasi dan mengusulkan calon yang memenuhi syarat sebagai anggota Direksi dan/atau anggota Dewan Komisaris kepada Dewan Komisaris untuk disampaikan kepada RUPS untuk mendapatkan persetujuan. 
    
    2)	Menelaah dan menentukan atas nama Dewan Komisaris: 
    
    (i)	Struktur, jumlah dan komposisi Direksi dan Dewan Komisaris yang sesuai. 
    (ii)	Rencana suksesi Direksi dan Dewan Komisaris. 
    (iii)	Kebijakan dan kriteria yang diperlukan dalam proses nominasi Direksi dan Dewan Komisaris. 
    (iv)	Kebijakan penilaian kinerja anggota Direksi dan anggota Dewan Komisaris. 
    
    
    
    (v)	Hasil penilaian kinerja anggota Direksi dan anggota Dewan Komisaris berdasarkan kriteria yang telah ditetapkan. 
    (vi)	Program pengembangan anggota Direksi dan anggota Dewan Komisaris. 
    
    b.	Terkait dengan Fungsi Remunerasi 
    
    1)	Dalam melaksanakan tugas dan tanggung jawabnya sebagaimana disebutkan dalam Bagian 2 di atas, Komite Nominasi dan Remunerasi wajib menelaah dan menentukan, atas nama Dewan Komisaris: 
    
    (i)	Struktur remunerasi bagi anggota Direksi dan/atau anggota Dewan Komisaris. 
    (ii)	Kebijakan remunerasi bagi anggota Direksi dan/atau anggota Dewan Komisaris. 
    (iii)	Remunerasi bagi masing-masing anggota Direksi dan/atau anggota Dewan Komisaris (kecuali ditentukan lain oleh RUPS). 
    (iv)	Penilaian kesesuaian remunerasi yang diterima masing-masing anggota Direksi dan anggota Dewan Komisaris terkait dengan kinerja mereka. 
    
    2)	Dalam penentuan struktur, kebijakan, dan besaran remunerasi tersebut, Komite Nominasi dan Remunerasi  harus memperhatikan hal-hal di bawah ini: 
    
    (i)	remunerasi yang berlaku pada industri sesuai dengan kegiatan usaha dan/atau skala usaha sejenis dari Perseroan; 
    (ii)	tugas, tanggung jawab dan wewenang anggota Direksi dan/atau anggota Dewan Komisaris dikaitkan dengan pencapaian tujuan dan kinerja Perseroan; 
    (iii)	target dan kinerja masing-masing anggota Direksi dan anggota Dewan Komisaris; dan
    (iv)	keseimbangan tunjangan yang bersifat tetap dan bersifat variabel. 
    
    3)	Struktur, kebijakan, dan Remunerasi sebagaimana tersebut di atas harus dievaluasi oleh Komite Nominasi dan Remunerasi paling kurang 1 (satu) kali dalam 1 (satu) tahun.
    
    c.	Komite Nominasi dan Remunerasi berwenang untuk menjalankan hal-hal lain yang dianggap perlu untuk melaksanakan tugas dan tanggung jawabnya sesuai dengan peraturan perundang-undangan yang berlaku.
    
    5.	PENYELENGGARAAN RAPAT 	
    
    a.	Jadwal 
    
    Rapat Komite Nominasi dan Remunerasi diselenggarakan secara berkala paling kurang 1 (satu) kali dalam 4 (empat) bulan.
    
    
     
    
    b.	Panggilan 
    
    1)	Panggilan rapat Komite Nominasi dan Remunerasi dilakukan oleh ketua Komite Nominasi dan Remunerasi atau salah seorang anggota Komite Nominasi dan Remunerasi. 
    
    2)	Panggilan rapat Komite Nominasi dan Remunerasi disampaikan kepada setiap anggota Komite Nominasi dan Remunerasi sekurang-kurangnya 10 (sepuluh) hari kalender dan untuk hal-hal mendesak sekurang-kurangnya 5 (lima) hari kalender sebelum rapat diadakan dengan tidak memperhitungkan tanggal panggilan dan tanggal rapat. 
    
    3)	Komite Nominasi dan Remunerasi juga dapat mengundang pihak(-pihak) lain yang dianggap perlu sehubungan dengan pelaksanaan tugasnya untuk hadir dalam rapat Komite Nominasi dan Remunerasi. 
    
    
    4)	Panggilan rapat harus mencantumkan acara, tanggal, waktu dan tempat rapat. 
    
    5)	Rapat Komite Nominasi dan Remunerasi diadakan di tempat kedudukan Perseroan atau tempat kegiatan usaha Perseroan. Apabila semua anggota Komite Nominasi dan Remunerasi hadir atau diwakili, panggilan terlebih dahulu tesebut tidak disyaratkan dan rapat Komite Nominasi dan Remunerasi dapat diadakan dimanapun juga dan berhak mengambil keputusan yang sah dan mengikat. 
    
    6)	Rapat Komite Nominasi dan Remunerasi dapat dilakukan juga melalui video konferensi atau sarana media elektronik lainnya yang memungkinkan semua peserta rapat dapat saling melihat dan mendengar secara langsung serta berpartisipasi dalam rapat. 
    
    c.	Pimpinan dan Keputusan Rapat 
    
    1)	Rapat Komite Nominasi dan Remunerasi dipimpin oleh ketua Komite Nominasi dan Remunerasi. 
    
    2)	Rapat Komite Nominasi dan Remunerasi hanya dapat diselenggarakan apabila dihadiri oleh mayoritas dari jumlah anggota Komite Nominasi dan Remunerasi, termasuk ketua Komite Nominasi dan Remunerasi. 
    
    3)	Keputusan rapat Komite Nominasi dan Remunerasi diambil berdasarkan musyawarah mufakat. Dalam hal keputusan berdasarkan musyawarah mufakat tidak tercapai, pengambilan keputusan dilakukan berdasarkan suara terbanyak. 
    
    4)	Jika dalam pemungutan suara terjadi suara yang berimbang, maka usul dianggap ditolak. 
    
    
    
    
    5)	Dalam hal terdapat perbedaan pendapat dalam proses pemungutan suara, maka perbedaan pendapat tersebut wajib dimuat dalam risalah rapat beserta alasan perbedaan pendapat tersebut. 
    
    6)	Risalah rapat Komite Nominasi dan Remunerasi dibuat oleh sekretaris Komite Nominasi dan Remunerasi. Dalam hal sekretaris Komite Nominasi dan Remunerasi tidak hadir maka risalah rapat dibuat oleh salah seorang yang hadir dalam rapat yang ditunjuk oleh ketua rapat Komite Nominasi dan Remunerasi. Risalah tersebut kemudian harus ditandatangani oleh ketua rapat Komite Nominasi dan Remunerasi dan salah seorang anggota Komite Nominasi dan Remunerasi yang hadir dalam rapat yang ditunjuk untuk maksud tersebut guna memastikan kelengkapan dan kebenaran risalah rapat. 
    
    7)	Hasil rapat Komite Nominasi dan Remunerasi wajib didokumentasikan oleh Perseroan dalam risalah rapat.
     
    8)	Bahan dan risalah rapat wajib dijaga kerahasiaannya. 
    
    9)	Komite Nominasi dan Remunerasi dapat juga mengambil keputusan yang sah tanpa mengadakan rapat Komite Nominasi dan Remunerasi, dengan ketentuan semua anggota Komite Nominasi dan Remunerasi telah diberitahu secara tertulis dan semua anggota Komite Nominasi dan Remunerasi memberikan persetujuan secara tertulis mengenai usul yang diajukan serta menandatangani keputusan tersebut. 
    
    6.	SISTEM PELAPORAN KEGIATAN
        
    a.	Komite Nominasi dan Remunerasi harus melaporkan pelaksanaan tugas, tanggung jawab, dan prosedur Nominasi dan Remunerasi yang dijalankan kepada kepada Dewan Komisaris. 
    
    b.	Laporan tersebut merupakan bagian dari laporan pelaksanaan tugas Dewan Komisaris  yangakan dimuat dalam Laporan Tahunan Perseroan dan disampaikan dalam RUPS
    
    7.	MASA JABATAN, PENGANGKATAN DAN TATACARA PENGGANTIAN ANGGOTA 
    
    a.	Anggota Komite Nominasi dan Remunerasi diangkat dan diberhentikan berdasarkan keputusan Dewan Komisaris. Anggota Komite Nominasi dan Remunerasi diangkat untuk masa jabatan terhitung sejak tanggal sebagaimana ditentukan oleh Dewan Komisaris sampai dengan penutupan RUPS Tahunan yang ketiga sejak tanggal tersebut, dengan mengindahkan ketentuan yang berlaku. 
    
    
    
    
    
    
    
    
    b.	Jabatan anggota Komite Nominasi dan Remunerasi berakhir apabila anggota tersebut: 
    
    1)	mengundurkan diri; 
    2)	tidak lagi memenuhi persyaratan perundang-undangan yang berlaku; 
    3)	meninggal dunia; atau 
    4)	diberhentikan berdasarkan keputusan Dewan Komisaris. 
    
    c.	Anggota Komite Nominasi dan Remunerasi yang masa jabatannya telah berakhir dapat diangkat kembali. 
    
    d.	Seorang anggota Komite Nominasi dan Remunerasi dapat mengundurkan diri dari jabatannya dengan memberitahukan secara tertulis mengenai maksud tersebut kepada Perseroan sekurang-kurangnya 30 (tiga puluh) hari kalender sebelum tanggal pengunduran dirinya. Jika pengunduran diri anggota Komite Nominasi dan Remunerasi tersebut mengakibatkan jumlah anggota Komite Nominasi dan Remunerasi menjadi kurang dari 3 (tiga), maka Dewan Komisaris wajib menyelenggarakan rapat paling lambat 60 (enam puluh) hari kalender setelah diterimanya pemberitahuan tertulis pengunduran diri tersebut untuk mengangkat orang lain sebagai pengganti anggota Komite Nominasi dan Remunerasi yang mengundurkan diri tersebut. 

