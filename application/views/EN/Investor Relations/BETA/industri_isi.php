<h3>PROGRAM <span>ANTV</span></h3>
        <p>
        Sebagai family entertainment TV channel, ANTV menghadirkan berbagai program untuk keluarga Indonesia di semua kalangan & semua usia. Agar mampu menjangkau semua pemirsanya, ANTV mengkategorikan program-programnya untuk 4 target pemirsa dengan genre & waktu tayang yang disesuaikan :
        </p>
        
        
